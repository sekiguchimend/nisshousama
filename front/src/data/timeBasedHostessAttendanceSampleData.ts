// 時間軸のラベル表示
export const getTimeLabel = (hour: number): string => {
  if (hour === 0) return '24';
  return hour.toString();
};

// 時間の位置を計算（ピクセル単位）
export const getTimePosition = (time: string, totalWidth: number = 800): number => {
  const [hours, minutes] = time.split(':').map(Number);
  const hour24 = hours === 0 ? 24 : hours; // 0時は24時として扱う
  const totalMinutes = (hour24 - 6) * 60 + minutes; // 6時を基準
  const totalAvailableMinutes = (24 - 6 + 5) * 60; // 6時～翌5時までの時間
  return (totalMinutes / totalAvailableMinutes) * totalWidth;
};

// 勤務時間の長さを計算（ピクセル単位）
export const getDurationWidth = (startTime: string, endTime: string, totalWidth: number = 800): number => {
  const startPos = getTimePosition(startTime, totalWidth);
  const endPos = getTimePosition(endTime, totalWidth);
  return Math.max(0, endPos - startPos);
};
